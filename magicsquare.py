
def magic_solver(m):
    ms = [[8,1,6,3,5,7,4,9,2],[6,1,8,7,5,3,2,9,4],[4,9,2,3,5,7,8,1,6],
          [2,9,4,7,5,3,6,1,8],[8,3,4,1,5,9,6,7,2],[4,3,8,9,5,1,2,7,6],
          [6,7,2,1,5,9,8,5,3],[2,7,6,9,5,1,4,3,8]]

    min = 999
    for i in range(0,8):
        val = 0
        for j in range(0,9):
            val += abs(m[int(j/3)][j%3]-ms[i][j])
        if val<min:
            min = val
    return min        



        
